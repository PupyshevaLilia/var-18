// Получаем предложение от пользователя
var sentence = prompt("Введите предложение:");

// Разделяем предложение на слова
var words = sentence.split(" ");

// Инициализируем переменную для хранения самого длинного слова
var longestWord = "";

// Перебираем все слова в предложении
for (var i = 0; i < words.length; i++) {
  // Если текущее слово длиннее предыдущего самого длинного слова,
  // то обновляем значение самого длинного слова
  if (words[i].length > longestWord.length) {
    longestWord = words[i];
  }
}

// Выводим самое длинное слово
console.log("Самое длинное слово: " + longestWord);